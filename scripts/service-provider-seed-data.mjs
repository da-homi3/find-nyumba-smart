/**
 * NyumbaSearch — real Kenyan service provider directory seed (100 businesses).
 * Sourced from each business's public marketing / contact information.
 * verified = 1 → phone confirmed in public material; verified = 0 → use source_url until confirmed.
 */

export function directoryProviderUuid(spNumber) {
  const tail = String(spNumber).padStart(12, "0");
  const head = spNumber.toString(16).padStart(4, "0");
  return `c000${head}-0001-4000-8100-${tail}`;
}

/** @returns {import('./seed-service-providers.mjs').SeedProvider[]} */
function p(
  n,
  businessName,
  categories,
  areasServed,
  description,
  priceRange,
  phone,
  tier,
  verified,
  sourceUrl,
) {
  return {
    id: directoryProviderUuid(n),
    business_name: businessName,
    categories,
    areas_served: areasServed,
    description,
    price_range: priceRange,
    phone: phone || null,
    tier,
    status: "active",
    verified: verified ? 1 : 0,
    source_url: sourceUrl || null,
  };
}

